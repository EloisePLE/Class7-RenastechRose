package Day12_BreakandContSt;

public class Topic8_WhileLoopexample {

    public static void main(String[] args) {
        int start = 0; // starting point
        while(start<10){ // condition (or End point) // start<10 == true or false

            System.out.println("the number is "+start);
            start++; // increase or decrease
        }

        while(true){ // this will be true all the time
            System.out.println("Ziya"); // this will run until your computer gives up.
        }


    }
}
